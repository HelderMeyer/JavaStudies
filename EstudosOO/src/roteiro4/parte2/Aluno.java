package roteiro4.parte2;

public class Aluno {

	public int matricula;
	public String nome;
	public String curso;
	public int anoIngresso;
	
	Aluno(int matricula, String nome, String curso, int anoIngresso) {
		this.matricula = matricula;
		this.nome = nome;
		this.curso = curso;
		this.anoIngresso = anoIngresso;
	}
}
